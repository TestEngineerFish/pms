package com.einyun.app.pms.mine.module;

public class FeedBackBean {
    private String content;
    private String feedbackAccount;
    private String feedbackName;
    private String feedbackPhone;
    private String feedbackId;
    private int issueType;
    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public String getFeedbackAccount() {
        return feedbackAccount;
    }

    public void setFeedbackAccount(String feedbackAccount) {
        this.feedbackAccount = feedbackAccount;
    }

    public String getFeedbackName() {
        return feedbackName;
    }

    public void setFeedbackName(String feedbackName) {
        this.feedbackName = feedbackName;
    }

    public String getFeedbackPhone() {
        return feedbackPhone;
    }

    public void setFeedbackPhone(String feedbackPhone) {
        this.feedbackPhone = feedbackPhone;
    }

    public String getFeedbackId() {
        return feedbackId;
    }

    public void setFeedbackId(String feedbackId) {
        this.feedbackId = feedbackId;
    }

    public int getIssueType() {
        return issueType;
    }

    public void setIssueType(int issueType) {
        this.issueType = issueType;
    }


}
